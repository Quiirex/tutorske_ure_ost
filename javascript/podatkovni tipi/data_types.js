// V JavaScriptu imamo različne podatkovne tipe. Spodaj so navedeni najbolj pogosti primeri:
// JavaScript ima privzeto 7 "primitivnih" podatkovnih tipov in 1 "ne-primitivni" podatkovni tip:
// Number, String, Boolean, Undefined, Null, Object (ne-primitiven), Symbol in BigInt.

// 1. Number
var stevilo = 10; // To je številski podatkovni tip. Lahko vključuje celo število, decimalno število, pozitivno ali negativno število.

// 2. String
var niz = 'To je niz'; // To je niz. Lahko vključuje eno ali več črk, številk ali simbolov.

// 3. Boolean
var resnica = true; // To je boolean. Lahko je samo resnično (true) ali neresnično (false).

// 4. Undefined
var nedefinirano; // To je nedefinirano. To pomeni, da spremenljivka nima dodeljene vrednosti.

// 5. Null
var nic = null; // To je null. To pomeni, da spremenljivka nima vrednosti ali objekta.

// 6. Object
var objekt = { ime: 'Janez', priimek: 'Novak' }; // To je objekt. Objekt je zbirka lastnosti, kjer vsaka lastnost ima ime in vrednost.

// 7. Symbol
var simbol = Symbol('opis'); // To je simbol. Simbol predstavlja edinstveno vrednost.

// 8. BigInt
var velikoStevilo = 1234567890123456789012345678901234567890n; // To je BigInt. BigInt predstavlja celo število poljubne velikosti.
